#include <stdio.h>
/*
 * add - add two integers
 * @a: first int
 * @b: second int
 *
 * Return: addition final answer
 */
int add(int a, int b)
{
	return (a + b);
}

/*
 * sub - sub two integers
 * @a: first int
 * @b: second int
 *
 * Return: subtraction final result
 */
int sub(int a, int b)
{
	return (a - b);
}

/*
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 *
 * Return: multiplication final result
 */
int mul(int a, int b)
{
	return (a * b);
}

/*
 * div - divide two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: division final answer
 */
int div(int a, int b)
{
	return (a / b);
}

/*
 * mod - mod of two integer
 * @a: first int
 * @b: second int
 *
 * Return: modulo final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
